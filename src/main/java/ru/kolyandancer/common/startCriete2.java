package ru.kolyandancer.common;

public class startCriete2 {
    public static void main(String[] args) {
        int myInt = 557; // целые числа 32 бит от -2147483648 до 2147483647
        short myShort = 1234; // целые числа 16 бит (маленькие числа от -32 тыс до 32 тыс)
        long myLong = 987654; // целые числа 64 бит (большие числа)
        double myDouble = 235.35; // вещественные типы данных до 64 бит
        float myFloat = 1234.76f; // вещественные типы данных до 32 бит
        char c = 'a'; // символы (буквы) пишутся в одинарных ковычках
        boolean b = true; // логический тип данных (либо TRUE, либо FALSE)
        byte myByte = 100; // целые числа до 8 бит ( от -128 до 127)
        System.out.println(myInt);
        System.out.println(myShort);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(c);
        System.out.println(b);
        System.out.println(myByte);
    }
}
